assertEquals(path.posix.basename(""), "")
assertEquals(path.posix.basename("/dir/basename.ext"), "basename.ext")
assertEquals(path.posix.basename("/basename.ext"), "basename.ext")
assertEquals(path.posix.basename("basename.ext"), "basename.ext")
assertEquals(path.posix.basename("basename.ext/"), "basename.ext")
assertEquals(path.posix.basename("basename.ext//"), "basename.ext")
assertEquals(path.posix.basename("aaa/bbb", "/bbb"), "bbb")
assertEquals(path.posix.basename("aaa/bbb", "a/bbb"), "bbb")
assertEquals(path.posix.basename("aaa/bbb", "bbb"), "bbb")
assertEquals(path.posix.basename("aaa/bbb//", "bbb"), "bbb")
assertEquals(path.posix.basename("aaa/bbb", "bb"), "b")
assertEquals(path.posix.basename("aaa/bbb", "b"), "bb")
assertEquals(path.posix.basename("/aaa/bbb", "/bbb"), "bbb")
assertEquals(path.posix.basename("/aaa/bbb", "a/bbb"), "bbb")
assertEquals(path.posix.basename("/aaa/bbb", "bbb"), "bbb")
assertEquals(path.posix.basename("/aaa/bbb//", "bbb"), "bbb")
assertEquals(path.posix.basename("/aaa/bbb", "bb"), "b")
assertEquals(path.posix.basename("/aaa/bbb", "b"), "bb")
assertEquals(path.posix.basename("/aaa/bbb"), "bbb")
assertEquals(path.posix.basename("/aaa/"), "aaa")
assertEquals(path.posix.basename("/aaa/b"), "b")
