local path = require("path")

local __filename = "test-path-basename.js"

assertEquals(path.win32.basename(__filename), "test-path-basename.js")
assertEquals(path.win32.basename(__filename, ".js"), "test-path-basename")
assertEquals(path.win32.basename(".js", ".js"), "")
assertEquals(path.win32.basename(""), "")
assertEquals(path.win32.basename("/dir/basename.ext"), "basename.ext")
assertEquals(path.win32.basename("/basename.ext"), "basename.ext")
assertEquals(path.win32.basename("basename.ext"), "basename.ext")
assertEquals(path.win32.basename("basename.ext/"), "basename.ext")
assertEquals(path.win32.basename("basename.ext//"), "basename.ext")
assertEquals(path.win32.basename("aaa/bbb", "/bbb"), "bbb")
assertEquals(path.win32.basename("aaa/bbb", "a/bbb"), "bbb")
assertEquals(path.win32.basename("aaa/bbb", "bbb"), "bbb")
assertEquals(path.win32.basename("aaa/bbb//", "bbb"), "bbb")
assertEquals(path.win32.basename("aaa/bbb", "bb"), "b")
assertEquals(path.win32.basename("aaa/bbb", "b"), "bb")
assertEquals(path.win32.basename("/aaa/bbb", "/bbb"), "bbb")
assertEquals(path.win32.basename("/aaa/bbb", "a/bbb"), "bbb")
assertEquals(path.win32.basename("/aaa/bbb", "bbb"), "bbb")
assertEquals(path.win32.basename("/aaa/bbb//", "bbb"), "bbb")
assertEquals(path.win32.basename("/aaa/bbb", "bb"), "b")
assertEquals(path.win32.basename("/aaa/bbb", "b"), "bb")
assertEquals(path.win32.basename("/aaa/bbb"), "bbb")
assertEquals(path.win32.basename("/aaa/"), "aaa")
assertEquals(path.win32.basename("/aaa/b"), "b")
assertEquals(path.win32.basename("/a/b"), "b")
assertEquals(path.win32.basename("//a"), "a")
assertEquals(path.win32.basename("a", "a"), "")

assertEquals(path.posix.basename(__filename), "test-path-basename.js")
assertEquals(path.posix.basename(__filename, ".js"), "test-path-basename")
